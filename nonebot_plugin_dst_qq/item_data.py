# 反向映射 (中文 -> 英文)
CHINESE_TO_ENGLISH = {v: k for k, v in ITEM_NAME_MAPPING.items()}

def search_items(keyword: str, limit: int = 10):
    """
    搜索物品
    支持中英文搜索，模糊匹配
    """
    keyword = keyword.lower().strip()
    results = []
    
    # 精确匹配优先
    for english, chinese in ITEM_NAME_MAPPING.items():
        if keyword == english.lower() or keyword == chinese:
            results.append({
                'english_name': english,
                'chinese_name': chinese
            })
    
    # 如果没有精确匹配，进行模糊匹配
    if not results:
        for english, chinese in ITEM_NAME_MAPPING.items():
            if (keyword in english.lower() or 
                keyword in chinese or
                any(keyword in part for part in english.lower().split('_'))):
                results.append({
                    'english_name': english,
                    'chinese_name': chinese
                })
                
                if len(results) >= limit:
                    break
    
    return results[:limit]

def get_item_chinese_name(english_name: str) -> str:
    """获取物品中文名"""
    return ITEM_NAME_MAPPING.get(english_name, english_name)

def get_item_english_name(chinese_name: str) -> str:
    """获取物品英文名"""
    return CHINESE_TO_ENGLISH.get(chinese_name, chinese_name)

def get_total_count() -> int:
    """获取物品总数量"""
    return len(ITEM_NAME_MAPPING)

